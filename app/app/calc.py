def add(x, y):
    """_summary_

    Args:
        x (number): First number
        y (number): Second number

    Returns:
        number: The sum of two numbers
    """
    return x + y


def subtract(x, y):
    """_summary_

    Args:
        x (number): First number
        y (number): Second number

    Returns:
        number: The difference between first and second numbers
    """
    return x - y
